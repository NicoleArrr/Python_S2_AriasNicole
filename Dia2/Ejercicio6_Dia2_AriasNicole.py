# Algoritmo para determinar si un número es par o impar

# Entrada por consola
numero = int(input("Ingresa un número: "))

# Se establece la condición
if (numero % 2==0):
    # Salida por conosola
    print(numero, " Es número par")
elif (numero % 2 != 0):
    print(numero, " No Es número par")
else:
    print ("No es posible realizar la determinación de número par")

