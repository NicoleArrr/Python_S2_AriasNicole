# Algoritmo para convertir grados Celsius a Fahrenheit

# Entrada por consola
gradosC = float(input("Ingresa la tempertaura en grados Celsius (°C):  "))
gradosF = float

# Se enuncia la fórmula donde será reemplazado el valor conocido para hallar el desconocido
gradosF = (gradosC*1.8)+32

# Salida por consola
print (gradosC, "(°C) equivalen a ", gradosF, "(°F) grados Fahrenheit")

