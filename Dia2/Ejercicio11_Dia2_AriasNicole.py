# Algoritmo para calcular el área de  un círculo

# Entrada por consola
radio = float (input ("Ingresa el radio de la circunferencia: "))

# Se define el valor de la variable
pi = 3.14159265359

# Se enuncia la fórmula donde serán reemplazados los valores conocido para hallar el desconocido
area = (pi * radio) **2

# Salidas por consola
print ("Calcula el área de un círculo")
print ("Fórmula =  π * r ²")
print ("Pi: ", pi , "Radio: ", radio, "²")
print (area)