# Algoritmo para calcular el área de un triángulo

# Entrada por consola
base = float (input ("Ingresa la medida de la base: "))
high = float ( input ("Ingresa la medida de la altura: "))

# Se enuncia la fórmula donde serán reemplazados los valores conocido para hallar el desconocido
area = (base*high)/2

# Salidas por consola
print ("Calcula el área de un triángulo")
print ("Base: ", base, "Altura: ", high, )
print ("Fórmula = base * altura / 2")
print (area)

